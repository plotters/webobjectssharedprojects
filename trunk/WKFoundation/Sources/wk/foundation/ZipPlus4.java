package wk.foundation;

/**
 * A simple class to represent a ZipPlus4 as an object since we so commonly work with this object type
 * @author kieran
 *
 */
public class ZipPlus4 {
	final private Integer zip5Part;
	final private Integer plus4Part;

	public ZipPlus4(Integer zip5Part, Integer plus4Part) {
		this.zip5Part = zip5Part;
		this.plus4Part = plus4Part;
	}


}
